function calcular(){
var a = Number(prompt("Digite um valor"));
var b = Number(prompt("Digite outro valor"));

var soma = a + b;
alert("A  resposta é:" +soma);

var subtracao = a - b;
alert("A resposta é:" -subtracao);

var multiplicacao = a * b
alert("A resposta é:" *multiplicacao);

var divisao = a/b
alert("A resposta é:" /divisao);

}
calcular();